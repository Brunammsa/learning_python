"""
Erros mais comuns:

- SyntaxError: Ocorre quando o python encontra um erro de sintaxe, ou seja, o python não reconheceu como parte da linguagem;

- NameError: Ocorre quando uma variável ou função não fi definida;

- TypeError: Ocorre quando uma função/operação/função é aplicada a um tipo errada;

- IndexError: Ocorre quando tentamos acessar um elemento em uma lista ou outro tipo de dado indexado utilizando um índice inválido;

- ValueError: Ocorre quando uma função ou operação built-in (integrada), recebe um argumento com tipo correto mas valor inapropriado;

- KeyError: Ocorre quando tentamos acessar um dict com uma chave que não existe;

- AttributeError: Ocorre quando uma variável não tem um atributo ou função;

- IdentationError: Ocorre quando não respeitamos a indentação do Python (4 espaços);

OBS.: Exceptions e erros são sinônimos
"""