dias_semana = int(input("Digite um número de 1 a 7: "))

if dias_semana == 2:
    print("Segunda feira")
elif dias_semana == 3:
    print("terça feira")
elif dias_semana == 4:
    print("Quarta feira")
elif dias_semana == 5:
    print("Quinta feira")
elif dias_semana == 6:
    print("Sexta feira")
elif dias_semana == 7:
    print("Sábado")
elif dias_semana == 1:
    print("Domingo")
else:
    print("Data inválida, digite um número de 1 a 7")
